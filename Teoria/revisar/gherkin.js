/*
    O Gherkin é uma sintaxe para escrita de cenários.
    Se baseando na regra de negócio, os cenários escritos em Gherkin são descomplicados e podem ser compreendidos por qualquer membro do time.

    Cada cenário deverá ter um nome, diferenciando cada ação de usuário.
    Escrita por algumas palavrinhas chaves, o Gherkin é moldado da seguinte maneira, Exemplo:

    Cenário: <nome do cenário>

        Dado ➔ é a pré-condição para uma determinada ação;
        Quando ➔ é a ação que o usuário irá realizar;
        Então ➔ é o resultado esperado daquela ação.

        Há algumas palavras chaves extras, como por exemplo:
        E ➔ Pode indicar uma ação adicional;

        Ou ➔ Indica uma outra ação, do tipo ‘apertar botão Entrar ou Realizar login’.
*/